#include <stdio.h>

int main() {
    // Expression that uses values
    int sum1 = 20 + 40;

    // Defining variables
    int a = 20, b = 40;

    // Expression that uses variables
    int sum2 = a + b;

    // Output the results
    printf("Sum of 2 numbers is: %d", sum1);
    printf("\n\nSum of another 2 numbers is: %d", sum2);

    return 0;
}


